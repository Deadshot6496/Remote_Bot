static void blinky_1(void *parameters)
{
	while(1)
	{
		xSemaphoreTake(SimpleMutex, portMAX_DELAY);
		if(completeRx)
		{

			uint8_t prop = atoi(buffers.rx);
			TIM2->CCR1 = prop;
			TIM2->CCR2 = prop;
			if(buffers.rx[5] == '1')
			{
				left();
			}
			else if(buffers.rx[5] == '2')
			{
				right();
			}
			else
			{
				stop_Steer();
			}
			if(buffers.rx[3] == '1')
			{
				fwd();
			}
			else if(buffers.rx[3] == '2')
			{
				rev();
			}
			else
			{
				stop();
			}
			completeRx = false;
		}
		xSemaphoreGive(SimpleMutex);


	}
}
